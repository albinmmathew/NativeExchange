    @SuppressWarnings("unchecked")
        
    private void loadProfileInfo() {
        Connection con = DBConnect.getConnection();
        String query = "SELECT username FROM users WHERE user_id = ?";
        PreparedStatement pst;
        try  {
            pst = con.prepareStatement(query);
            pst.setInt(1, this.userId);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                // Set the username from DB
                username.setText(rs.getString("username"));

                // Map the roleId to a String
                String roleName;
                switch (this.roleId) {
                    case 1 -> roleName = "Admin";
                    case 2 -> roleName = "Vendor";
                    case 3 -> roleName = "Customer";
                    default -> roleName = "Unknown";
                }
                role.setText(roleName);

                // Ensure fields are locked
                username.setEditable(false);
                role.setEditable(false);
            }
        } catch (SQLException e) {
            logger.log(java.util.logging.Level.SEVERE, "Database error", e);
        }
    }
    
