    private void applyRoleRestrictions() {
        if (roleId == 1) { // ADMIN
            vendorMenu.setVisible(false);
        }
        else if (roleId == 2) { // VENDOR
            adminMenu.setVisible(false);
            listingsMenu.setVisible(false); // customer listing menu
        }
        else if (roleId == 3) { // CUSTOMER
            adminMenu.setVisible(false);
            vendorMenu.setVisible(false);
        }
    }

